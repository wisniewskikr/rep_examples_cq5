package pl.kwi.utils;

public class FormUtilModel {
	
	
	boolean componentExists;
	
	
	public FormUtilModel(boolean componentExists) {
		this.componentExists = componentExists;
	}


	public boolean isComponentExists() {
		return componentExists;
	}
	public void setComponentExists(boolean componentExists) {
		this.componentExists = componentExists;
	}	
		

}
